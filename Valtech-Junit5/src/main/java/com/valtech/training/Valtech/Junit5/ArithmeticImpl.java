package com.valtech.training.Valtech.Junit5;

public class ArithmeticImpl implements Arithmetic {
public int add(int a, int b) {
		
		return a+b;
	}

	public int sub(int a, int b) {
	
		return a-b;
	}

	public int mul(int a, int b) {
	
		return a*b;
	}

	public int div(int a, int b) {
	
		return a/b;
	}

}
